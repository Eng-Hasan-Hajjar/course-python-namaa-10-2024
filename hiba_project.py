# دالة لعرض الفكرة الرئيسية
def عرض_الفكرة():
    العنوان = القائمة.get()  # الحصول على العنوان المحدد من القائمة
    القصة = القصص.get(العنوان, {"ملخص": "لا توجد فكرة رئيسية."})
    رسالة_القصة.config(text=القصة["ملخص"])

# دالة لعرض النص الكامل
def عرض_القصة():
    العنوان = القائمة.get()  # الحصول على العنوان المحدد من القائمة
    القصة = القصص.get(العنوان, {"نص": "لا توجد قصة بهذا العنوان."})
    رسالة_القصة.config(text=القصة["نص"])

# إعداد واجهة المستخدم
root = tk.Tk()
root.title("قصص للأطفال")
root.geometry("800x600")

# إعداد الخلفية الملونة
background_image = Image.open("background.png")  # ضع اسم الصورة هنا
background_image = background_image.resize((800, 600), Image.ANTIALIAS)
background_photo = ImageTk.PhotoImage(background_image)

background_label = tk.Label(root, image=background_photo)
background_label.place(relwidth=1, relheight=1)

# قائمة العناوين
القائمة = ttk.Combobox(
    root,
    values=list(القصص.keys()),
    font=("Comic Sans MS", 14)
)
القائمة.set("اختر عنوان القصة")  # النص الافتراضي
القائمة.pack(pady=20)

# إطار النص
رسالة_القصة = tk.Label(
    root,
    text="",
    font=("Comic Sans MS", 18),
    wraplength=700,
    bg="#FFE4C4",
    fg="#000",
    justify="left",
    padx=20,
    pady=20
)
رسالة_القصة.pack(pady=20)

# زر عرض الفكرة
زر_الفكرة = tk.Button(
    root,
    text="عرض الفكرة الرئيسية",
    font=("Arial", 14, "bold"),
    bg="#FF6347",
    fg="white",
    padx=20,
    pady=10,
    command=عرض_الفكرة
)
زر_الفكرة.pack(pady=10)

# زر عرض القصة
زر_القصة = tk.Button(
    root,
    text="عرض القصة الكاملة",
    font=("Arial", 14, "bold"),
    bg="#32CD32",
    fg="white",
    padx=20,
    pady=10,
    command=عرض_القصة
)
زر_القصة.pack(pady=10)

root.mainloop()